package JAVA_LOOPS;
public class PositiveNegative {
    public static void main(String[] args) {
        int number = 109 ; 
        if (number > 0 ) {
            System.out.println(number + " é positivo!");
        } else if( number < 0 ) {
            System.out.println(number + "é negativo!");
        }else {
            System.out.println(number + "não é positivo ou negativo!");
        }
    }
}
