package JAVA_CONVERSIONS;
/* We can convert char to int in java using various ways. If we direct assign char variable to int, it will
return ASCII value of given character.
*/

public class ConvASCII {
    public static void main(String[] args) {
        char c = 'a' ;
        char c2 = '1' ;
        int a = c ;
        int b = c2 ; 
        System.out.println(a);
        System.out.println(b);
    }
}
